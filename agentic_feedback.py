# LM Studio endpoint configuration
# ---------------------------------------------------------------------------
_LM_STUDIO_URL: str = "http://localhost:1234/v1/chat/completions"
_LM_STUDIO_HEALTH_URL: str = "http://localhost:1234/v1/models"

# Timeout (seconds) for the LLM HTTP call.
_REQUEST_TIMEOUT: int = 120

# ---------------------------------------------------------------------------
# JSON Schema for structured output (json_schema response_format mode).
# This version of LM Studio supports "json_schema" and "text" but NOT
# "json_object".  Providing a full schema lets the model know the exact
# shape expected and enforces it at the sampler level.
# ---------------------------------------------------------------------------
_RESPONSE_FORMAT_SCHEMA: dict = {
    "type": "json_schema",
    "json_schema": {
        "name": "obe_feedback",
        "strict": True,
        "schema": {
            "type": "object",
            "properties": {
                "breakdown": {"type": "string"},
                "study_schedule": {
                    "type": "array",
                    "items": {
                        "type": "object",
                        "properties": {
                            "week":  {"type": "integer"},
                            "focus": {"type": "string"},
                            "tasks": {
                                "type": "array",
                                "items": {"type": "string"},
                            },
                        },
                        "required": ["week", "focus", "tasks"],
                        "additionalProperties": False,
                    },
                },
            },
            "required": ["breakdown", "study_schedule"],
            "additionalProperties": False,
        },
    },
}


# ---------------------------------------------------------------------------
# Uniform result schema
# ---------------------------------------------------------------------------

@dataclass
class FeedbackResult:
    """
    Uniform output schema for the agentic layer.

    Always contains ``breakdown`` and ``study_schedule`` regardless of
    whether the LLM call succeeded or fell back to graceful degradation.
    The frontend can render this without branching on error states.

    Attributes
    ----------
    breakdown : str
        Natural-language analysis of the student's specific deficiencies.
    study_schedule : str | list
        Customised week-by-week study plan generated by the LLM.
    weak_areas : list[str]
        Machine-derived list of weak sub-topics fed into the LLM prompt.
    llm_available : bool
        True if the LM Studio call succeeded; False if fallback was used.
    latency_ms : float
        Wall-clock time of the LLM request in milliseconds.
    model_used : str
        Model identifier echoed from the LM Studio response, or "fallback".
    """
    breakdown: str
    study_schedule: str | list
    weak_areas: list[str]
    llm_available: bool = True
    latency_ms: float = 0.0
    model_used: str = "local-model"

    def to_dict(self) -> dict:
        return {
            "breakdown":      self.breakdown,
            "study_schedule": self.study_schedule,
            "weak_areas":     self.weak_areas,
            "llm_available":  self.llm_available,
            "latency_ms":     round(self.latency_ms, 1),
            "model_used":     self.model_used,
        }


# ---------------------------------------------------------------------------
# Automatic weak-area extraction (deterministic — no LLM required)
# ---------------------------------------------------------------------------

# Score thresholds used to classify each dimension as weak
_WEAK_THRESHOLD    = 50.0   # below this → weak
_MARGINAL_THRESHOLD = 65.0  # below this → marginal (noted but not flagged as critical)


def extract_weak_areas(
    crisp_attainment: float,
    label: str,
    assignment_score: float,
    attendance: float,
    ca: float | None = None,
    lab_work: float | None = None,
    final_exam: float | None = None,
) -> list[str]:
    """
    Derive a prioritised list of specific weak sub-topics from the FIS
    inputs and output — no LLM call is required for this step.

    Logic
    -----
    1. If individual UI components (CA, Lab, Final) are available, compare
       them to identify the weakest academic strand.
    2. Evaluate attendance as an independent dimension.
    3. Map the overall attainment label to foundational gaps if severe.

    Parameters
    ----------
    crisp_attainment : float   — centroid FIS output [0, 100]
    label : str                — linguistic label (Poor / Developing / …)
    assignment_score : float   — composite score fed to FIS [0, 100]
    attendance : float         — attendance percentage [0, 100]
    ca : float | None          — Continuous Assessment score [0, 100]
    lab_work : float | None    — Lab Work score [0, 100]
    final_exam : float | None  — Final Exam score [0, 100]

    Returns
    -------
    list[str]
        Ordered list of weak sub-topic strings, most critical first.
        Returns ["No specific weak areas identified"] when the student
        is performing well across all dimensions.
    """
    weak: list[str] = []

    # ── UI component analysis (most specific — use when available) ─────────
    if ca is not None and lab_work is not None and final_exam is not None:
        components = {
            "Continuous Assessment (quizzes and in-class tests)": ca,
            "Lab Work and practical implementation exercises":     lab_work,
            "Final Examination preparation and revision strategy": final_exam,
        }
        # Sort by score ascending — most critical first
        for name, score in sorted(components.items(), key=lambda x: x[1]):
            if score < _WEAK_THRESHOLD:
                weak.append(f"{name} (score: {score:.1f}/100 — below passing threshold)")
            elif score < _MARGINAL_THRESHOLD:
                weak.append(f"{name} (score: {score:.1f}/100 — marginal, needs improvement)")

        # Identify the single weakest component even if none are below threshold
        if not weak:
            weakest_name, weakest_score = min(components.items(), key=lambda x: x[1])
            if weakest_score < 75.0:
                weak.append(
                    f"{weakest_name} (score: {weakest_score:.1f}/100 — "
                    f"lowest-scoring component, targeted revision recommended)"
                )

    # ── Assignment score composite analysis (xAPI path — no UI components) ─
    elif assignment_score < _WEAK_THRESHOLD:
        weak.append(
            f"Academic engagement and assignment performance "
            f"(composite score: {assignment_score:.1f}/100)"
        )
        if assignment_score < 30.0:
            weak.append(
                "Foundational subject knowledge — core concepts require "
                "re-study before tackling advanced topics"
            )

    # ── Attendance analysis ────────────────────────────────────────────────
    if attendance < _WEAK_THRESHOLD:
        weak.append(
            f"Class attendance and in-person participation "
            f"({attendance:.1f}% — significantly below recommended 75%)"
        )
        weak.append(
            "Missed lecture content and live demonstrations — "
            "requires active catch-up via recorded materials and peer notes"
        )
    elif attendance < _MARGINAL_THRESHOLD:
        weak.append(
            f"Attendance consistency ({attendance:.1f}% — "
            f"borderline; risk of missing cumulative lecture dependencies)"
        )

    # ── Overall attainment severity ────────────────────────────────────────
    if label in ("Poor", "Developing"):
        weak.append(
            "Time management and structured independent study habits — "
            "critical for students in the lower attainment bands"
        )
    if label == "Poor":
        weak.append(
            "Prerequisite knowledge gaps — a diagnostic review of "
            "foundational topics is strongly recommended before progressing"
        )

    if not weak:
        return ["No specific weak areas identified — student is performing well across all assessed dimensions"]

    return weak


# ---------------------------------------------------------------------------
# Robust JSON extraction helper
# ---------------------------------------------------------------------------

def _extract_json(content: str) -> dict:
    """
    Parse a JSON object from LLM output robustly.

    Strategy (tried in order):
    1. ``json.loads(strict=False)`` on the raw content — handles embedded
       control characters (newlines, tabs) inside string values, which is
       the most common cause of JSONDecodeError from smaller models.
    2. Strip markdown fences (```json ... ```) and retry.
    3. Regex-extract the outermost ``{...}`` block and retry — handles
       models that emit preamble text before the JSON object.

    Raises
    ------
    json.JSONDecodeError
        If all three strategies fail.
    """
    import re as _re

    # Strategy 1: strict=False tolerates literal control chars in strings
    try:
        return json.loads(content, strict=False)
    except json.JSONDecodeError:
        pass

    # Strategy 2: strip markdown fences
    stripped = content.strip()
    if stripped.startswith("```"):
        match = _re.search(r"```(?:json)?\s*([\s\S]+?)```", stripped)
        if match:
            try:
                return json.loads(match.group(1).strip(), strict=False)
            except json.JSONDecodeError:
                pass

    # Strategy 3: extract outermost { ... } block
    match = _re.search(r"\{[\s\S]*\}", content)
    if match:
        return json.loads(match.group(0), strict=False)

    # All strategies exhausted — let the caller handle it
    raise json.JSONDecodeError(
        "No valid JSON object found in LLM output", content, 0
    )


# ---------------------------------------------------------------------------
# LM Studio client
# ---------------------------------------------------------------------------

class LMStudioClient:
    """
    Thin HTTP client for LM Studio's OpenAI-compatible `/v1/chat/completions`
    endpoint.  Uses only the standard library (`urllib`) so no extra
    dependency is required beyond what is already in requirements.txt.

    All traffic is to 127.0.0.1 — nothing leaves the local machine.
    """

    def __init__(
        self,
        url: str = _LM_STUDIO_URL,
        timeout: int = _REQUEST_TIMEOUT,
    ) -> None:
        self.url     = url
        self.timeout = timeout

    def is_available(self) -> bool:
        """
        Probe the LM Studio /v1/models endpoint for a quick health check.
        Returns True if LM Studio is running and has a model loaded.
        """
        try:
            req = urllib.request.Request(_LM_STUDIO_HEALTH_URL)
            with urllib.request.urlopen(req, timeout=3) as resp:
                return resp.status == 200
        except Exception:
            return False

    def generate(
        self,
        system_prompt: str,
        user_prompt: str,
        temperature: float = 0.65,
    ) -> tuple[dict, str, float]:
        """
        Send a chat completion request and return the parsed response.

        Parameters
        ----------
        system_prompt : str
            Instruction context given to the model.
        user_prompt : str
            The actual assessment data payload.
        temperature : float
            Sampling temperature. 0.65 balances creativity with JSON
            structure compliance for instruction-tuned models.

        Returns
        -------
        tuple[dict, str, float]
            (parsed_json_dict, model_id_string, latency_ms)

        Raises
        ------
        urllib.error.URLError
            If LM Studio is unreachable (not running, wrong port, etc.)
        json.JSONDecodeError
            If the model output is not valid JSON despite response_format.
        ValueError
            If the response structure is missing expected OpenAI fields.
        """
        import re as _re

        # Three attempts in priority order:
        #   1. json_schema  — structured output enforced at sampler level (best)
        #   2. text         — plain text, we extract JSON ourselves (wide compat)
        #   3. no key       — omit response_format entirely (last resort)
        attempts = [
            {"response_format": _RESPONSE_FORMAT_SCHEMA},
            {"response_format": {"type": "text"}},
            {},
        ]

        last_exc: Exception | None = None

        for extra in attempts:
            payload: dict = {
                "model": "local-model",
                "messages": [
                    {"role": "system", "content": system_prompt},
                    {"role": "user",   "content": user_prompt},
                ],
                "temperature": temperature,
                **extra,
            }

            data = json.dumps(payload).encode("utf-8")
            req  = urllib.request.Request(
                self.url,
                data=data,
                headers={"Content-Type": "application/json"},
                method="POST",
            )

            try:
                t0 = time.perf_counter()
                with urllib.request.urlopen(req, timeout=self.timeout) as resp:
                    raw     = resp.read().decode("utf-8")
                    latency = (time.perf_counter() - t0) * 1000

                response_json = json.loads(raw)

                if "choices" not in response_json or not response_json["choices"]:
                    raise ValueError(
                        f"Unexpected LM Studio response structure: "
                        f"{list(response_json.keys())}"
                    )

                content  = response_json["choices"][0]["message"]["content"]
                model_id = response_json.get("model", "local-model")

                parsed = _extract_json(content)
                return parsed, model_id, latency

            except urllib.error.HTTPError as http_exc:
                body = ""
                try:
                    body = http_exc.read().decode("utf-8")[:300]
                except Exception:
                    pass
                last_exc = http_exc
                fmt = extra.get("response_format", {}).get("type", "none")
                print(
                    f"[agentic] response_format={fmt!r} rejected "
                    f"(HTTP {http_exc.code}) — trying next mode. Body: {body}"
                )
                continue

            except urllib.error.URLError as url_exc:
                last_exc = url_exc
                raise   # network failure — no point retrying

        raise RuntimeError(
            f"generate() exhausted all response_format attempts. "
            f"Last error: {last_exc}"
        )


