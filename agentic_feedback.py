_SYSTEM_PROMPT = """\
You are an expert AI educational consultant embedded in an Outcome-Based \
Education (OBE) assessment system. You receive a student's defuzzified \
attainment score from a Mamdani Fuzzy Inference System along with their \
specific identified weak areas.

Your response MUST be a valid JSON object with exactly two keys:

1. "breakdown" (string): A 2–4 paragraph natural language analysis that:
   - Explains what the FIS attainment score means in OBE terms
   - Pinpoints the specific sub-topic deficiencies listed in the weak areas
   - Explains the likely root causes (e.g., attendance gaps causing cumulative \
knowledge loss, practical skills lagging behind theory, etc.)
   - Is written directly to the faculty reviewing this report

2. "study_schedule" (list of objects): A week-by-week plan, each entry being \
an object with keys "week" (integer), "focus" (string), and "tasks" \
(list of strings). Generate 4 weeks minimum; 6 weeks if the student is in \
the Poor or Developing band.

Return ONLY the JSON object. No markdown fences, no preamble text.\
"""


def _build_user_prompt(
    crisp_attainment: float,
    label: str,
    assignment_score: float,
    attendance: float,
    weak_areas: list[str],
    fired_rules: list[str],
    ca: float | None,
    lab_work: float | None,
    final_exam: float | None,
) -> str:
    lines = [
        "=== STUDENT ASSESSMENT REPORT ===",
        f"FIS Attainment Score      : {crisp_attainment:.2f}%",
        f"Attainment Classification : {label}",
        f"Composite Assignment Score: {assignment_score:.2f}/100",
        f"Attendance Percentage     : {attendance:.1f}%",
    ]

    if ca is not None:
        lines += [
            "",
            "=== GRADE COMPONENT BREAKDOWN ===",
            f"Continuous Assessment : {ca:.1f}/100  (weight 30%)",
            f"Lab Work              : {lab_work:.1f}/100  (weight 20%)",
            f"Final Exam            : {final_exam:.1f}/100  (weight 50%)",
        ]

    lines += [
        "",
        "=== IDENTIFIED WEAK AREAS ===",
    ]
    for i, area in enumerate(weak_areas, 1):
        lines.append(f"{i}. {area}")

    if fired_rules:
        lines += [
            "",
            "=== FUZZY RULES THAT FIRED (diagnostic) ===",
        ]
        for rule in fired_rules[:6]:   # cap at 6 to avoid token bloat
            lines.append(f"• {rule}")

    lines += [
        "",
        "Generate the breakdown and study_schedule JSON now.",
    ]

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Graceful degradation fallback (LM Studio offline)
# ---------------------------------------------------------------------------

def _build_fallback_feedback(
    crisp_attainment: float,
    label: str,
    weak_areas: list[str],
) -> FeedbackResult:
    """
    Return a rule-based static feedback object when the LLM is unavailable.
    Uses the same FeedbackResult schema so the frontend never needs to branch.
    The ``llm_available`` flag signals the UI to show an offline indicator.
    """
    severity_map = {
        "Poor":         "significantly below the minimum attainment threshold",
        "Developing":   "below the expected attainment level",
        "Satisfactory": "at a satisfactory but improvable level",
        "Good":         "at a good level with room for refinement",
        "Excellent":    "at an excellent level",
    }
    description = severity_map.get(label, "at an undetermined level")

    breakdown = (
        f"The student's FIS-computed attainment score is {crisp_attainment:.2f}%, "
        f"which places them {description} according to the OBE attainment rubric. "
        f"The following areas were algorithmically identified as requiring attention: "
        f"{'; '.join(weak_areas[:3])}. "
        f"Note: Detailed AI-generated feedback is unavailable because LM Studio is "
        f"not currently running on this machine. Start LM Studio and load a model "
        f"to enable personalised narrative feedback."
    )

    weeks = 4 if label in ("Good", "Satisfactory", "Excellent") else 6
    schedule = [
        {
            "week": w,
            "focus": f"Week {w}: Address {weak_areas[min(w - 1, len(weak_areas) - 1)]}",
            "tasks": [
                "Review relevant lecture notes and textbook sections",
                "Attempt past exam questions on this topic",
                "Discuss with faculty or teaching assistant for clarification",
            ],
        }
        for w in range(1, weeks + 1)
    ]

    return FeedbackResult(
        breakdown=breakdown,
        study_schedule=schedule,
        weak_areas=weak_areas,
        llm_available=False,
        latency_ms=0.0,
        model_used="fallback",
    )


# ---------------------------------------------------------------------------
# Primary public API
# ---------------------------------------------------------------------------

def generate_feedback(
    result: "AssessmentResult",
    ui_inputs: "UIFuzzyInputs | None" = None,
    client: LMStudioClient | None = None,
) -> FeedbackResult:
    """
    Full pipeline: FIS result → weak-area extraction → LLM → FeedbackResult.

    This is the single function called by ``api_server.py``.  It chains
    ``extract_weak_areas`` and ``LMStudioClient.generate`` automatically,
    with graceful degradation if LM Studio is offline.

    Parameters
    ----------
    result : AssessmentResult
        Output of ``FuzzyAssessmentEngine.assess()``.
    ui_inputs : UIFuzzyInputs | None
        If provided, the individual CA / Lab / Final scores are used to
        generate more specific weak-area labels and a richer prompt.
        Pass None for the xAPI batch-scoring path.
    client : LMStudioClient | None
        Optional pre-constructed client (useful for testing with a mock).
        If None, a default client is created.

    Returns
    -------
    FeedbackResult
        Uniform schema — always contains breakdown and study_schedule,
        whether the LLM was available or not.
    """
    if client is None:
        client = LMStudioClient()

    # ── Extract weak areas deterministically ───────────────────────────────
    ca        = float(ui_inputs.continuous_assessment) if ui_inputs else None
    lab_work  = float(ui_inputs.lab_work)              if ui_inputs else None
    final_exam = float(ui_inputs.final_exam)           if ui_inputs else None

    weak_areas = extract_weak_areas(
        crisp_attainment=result.crisp_attainment,
        label=result.label,
        assignment_score=result.assignment_score,
        attendance=result.attendance,
        ca=ca,
        lab_work=lab_work,
        final_exam=final_exam,
    )

    # ── Build prompts ───────────────────────────────────────────────────────
    user_prompt = _build_user_prompt(
        crisp_attainment=result.crisp_attainment,
        label=result.label,
        assignment_score=result.assignment_score,
        attendance=result.attendance,
        weak_areas=weak_areas,
        fired_rules=result.fired_rules,
        ca=ca,
        lab_work=lab_work,
        final_exam=final_exam,
    )

    # ── Call LM Studio ──────────────────────────────────────────────────────
    try:
        parsed, model_id, latency = client.generate(
            system_prompt=_SYSTEM_PROMPT,
            user_prompt=user_prompt,
        )

        # Normalise: LLM may return breakdown/study_schedule at any nesting level
        breakdown      = parsed.get("breakdown", "")
        study_schedule = parsed.get("study_schedule", [])

        if not breakdown:
            raise ValueError("LLM response missing 'breakdown' key.")

        return FeedbackResult(
            breakdown=breakdown,
            study_schedule=study_schedule,
            weak_areas=weak_areas,
            llm_available=True,
            latency_ms=latency,
            model_used=model_id,
        )

    except urllib.error.URLError as exc:
        print(
            f"[agentic] LM Studio network error ({type(exc).__name__}: {exc.reason}) "
            f"— returning rule-based fallback.\n"
            f"          Ensure LM Studio is open and the server is started "
            f"(Developer tab → Start Server)."
        )
        return _build_fallback_feedback(result.crisp_attainment, result.label, weak_areas)

    except (json.JSONDecodeError, ValueError, KeyError) as exc:
        print(f"[agentic] LLM output parse error ({type(exc).__name__}: {exc}) — returning rule-based fallback.")
        return _build_fallback_feedback(result.crisp_attainment, result.label, weak_areas)

    except Exception as exc:
        print(f"[agentic] Unexpected error ({type(exc).__name__}: {exc}) — returning rule-based fallback.")
        return _build_fallback_feedback(result.crisp_attainment, result.label, weak_areas)


