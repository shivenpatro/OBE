"""
agentic_feedback.py
===================
Agentic LLM layer for the OBE assessment pipeline.

Architecture
------------
This module sits between the Mamdani FIS (fuzzy_engine.py) and the
frontend dashboard.  It receives an AssessmentResult plus the raw UI
scores, automatically extracts the student's specific weak areas, then
sends a structured context to a locally running LM Studio instance and
returns a uniform FeedbackResult.

DATA PRIVACY & ARCHITECTURE DEFENCE
-------------------------------------
1. Zero-Egress Guarantee
   All inference runs on the local machine (LM Studio on the RTX 3050 GPU).
   No student score, name, or identifier is transmitted to any third-party
   cloud API, telemetry endpoint, or analytics service.  The HTTP request
   never leaves 127.0.0.1.

2. Regulatory Compliance by Design
   Because data residency is bound to the physical host:
   • FERPA  — student educational records never leave the institution's device.
   • GDPR   — no personal data crosses a network boundary; no DPA required.
   • COPPA  — minors' assessment data cannot be harvested by ad-tech pipelines.
   This makes the architecture defensible in any IRB or data-governance review.

3. Deterministic Latency
   GPU inference on a local model is immune to cloud rate-limits, quota
   exhaustion, and WAN jitter.  Response time is bounded by the GPU VRAM
   bandwidth, not by network round-trip time.

4. Model Auditability
   The faculty or institution controls exactly which model weights are loaded
   in LM Studio — no opaque model versioning or silent provider-side updates.

Flow
----
  AssessmentResult + UIFuzzyInputs
       │
       ▼
  extract_weak_areas()          — deterministic, score-driven analysis
       │
       ▼
  LMStudioClient.generate()    — POST to localhost:1234/v1/chat/completions
       │
       ▼
  FeedbackResult                — uniform schema regardless of LLM status
"""

